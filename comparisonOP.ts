//Comparison Operator are used to compare values of variables.

let value1 = 25
let value2 = 20
//Equals to
console.log(value1 == value2);    //false
console.log(value1 === value2);   //false
//Not equals to
console.log(value1 != value2);    //true
//Greater than
console.log(value1 > value2);     //true
//Less than
console.log(value1 < value2);     //false
//Greater than equals to
console.log(value1 >= value2);    //true
//Less than equals to
console.log(value1 <= value2);    //false